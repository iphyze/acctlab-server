function cashExcelColumn(int $index): string
{
    return Coordinate::stringFromColumnIndex($index);
}

function cashExcelTransactionLabel(string $type): string
{
    $labels = [
        'CASH_RECEIPT' => 'Additional cash received',
        'OPENING_BALANCE' => 'Opening balance',
        'DIRECT_DISBURSEMENT' => 'Direct cash disbursement',
        'IOU_DISBURSEMENT' => 'IOU / cash advance',
        'CASH_RETURN' => 'IOU retirement cash returned',
        'REIMBURSEMENT' => 'IOU reimbursement',
        'MUTILATED_CASH_SET_ASIDE' => 'Mutilated cash classified',
        'MUTILATED_CASH_REPLACEMENT' => 'Bank replacement of mutilated cash',
        'MUTILATED_CASH_BANK_RETURN' => 'Mutilated cash returned to bank',
        'REVERSAL' => 'Controlled reversal',
    ];

    $normalized = strtoupper($type);
    return $labels[$normalized] ?? ucwords(strtolower(str_replace('_', ' ', $normalized)));
}

