function cashExcelStyleRows(Worksheet $sheet, int $firstRow, int $lastRow, int $lastColumn): void
{
    if ($lastRow < $firstRow) {
        return;
    }

    $endColumn = cashExcelColumn($lastColumn);
    $sheet->getStyle("A{$firstRow}:{$endColumn}{$lastRow}")->applyFromArray([
        'font' => ['size' => 9, 'color' => ['rgb' => CASH_REPORT_TEXT]],
        'alignment' => ['vertical' => Alignment::VERTICAL_CENTER, 'wrapText' => true],
        'borders' => [
            'bottom' => ['borderStyle' => Border::BORDER_HAIR, 'color' => ['rgb' => CASH_REPORT_BORDER]],
        ],
    ]);

    for ($row = $firstRow; $row <= $lastRow; $row++) {
        $sheet->getRowDimension($row)->setRowHeight(23);
        if (($row - $firstRow) % 2 === 1) {
            $sheet->getStyle("A{$row}:{$endColumn}{$row}")->getFill()
                ->setFillType(Fill::FILL_SOLID)
                ->getStartColor()->setRGB('F4F9FA');
        }
    }
}

function cashExcelMoneyFormat(Worksheet $sheet, string $range): void
{
    $sheet->getStyle($range)->getNumberFormat()->setFormatCode('#,##0.00;[Red]-#,##0.00;–');
    $sheet->getStyle($range)->getAlignment()->setHorizontal(Alignment::HORIZONTAL_RIGHT);
}

function cashExcelSetWidths(Worksheet $sheet, array $widths): void
{
    foreach ($widths as $column => $width) {
        $sheet->getColumnDimension($column)->setWidth($width);
    }
}

function cashExcelStatusStyle(Worksheet $sheet, string $column, int $firstRow, int $lastRow): void
{
    for ($row = $firstRow; $row <= $lastRow; $row++) {
        $status = strtoupper((string) $sheet->getCell($column . $row)->getValue());
        if ($status === '') {
            continue;
        }
        $fill = CASH_REPORT_BLUE_SOFT;
        $font = CASH_REPORT_TEAL_DARK;
        if (in_array($status, ['PENDING', 'PENDING_RETURN', 'OVERDUE', 'PARTIAL', 'PARTIALLY_RETIRED'], true)) {
            $fill = CASH_REPORT_AMBER_SOFT;
            $font = '9A6500';
        } elseif (in_array($status, ['RETURNED', 'REPLACED', 'RECEIVED', 'CLOSED', 'ACTIVE'], true)) {
            $fill = CASH_REPORT_GREEN_SOFT;
            $font = CASH_REPORT_GREEN;
        } elseif (in_array($status, ['REVERSED', 'ARCHIVED', 'FAILED'], true)) {
            $fill = CASH_REPORT_RED_SOFT;
            $font = CASH_REPORT_RED;
        }
        $sheet->getStyle($column . $row)->applyFromArray([
            'font' => ['bold' => true, 'color' => ['rgb' => $font]],
            'fill' => ['fillType' => Fill::FILL_SOLID, 'startColor' => ['rgb' => $fill]],
            'alignment' => ['horizontal' => Alignment::HORIZONTAL_CENTER],
        ]);
    }
}

function cashExcelMetricCard(Worksheet $sheet, string $range, string $labelCell, string $valueCell, string $label, float $value, string $fill): void
{
    [$start, $end] = Coordinate::rangeBoundaries($range);
    $startColumn = cashExcelColumn((int) $start[0]);
    $endColumn = cashExcelColumn((int) $end[0]);
    $startRow = (int) $start[1];
    $endRow = (int) $end[1];
    $sheet->mergeCells("{$startColumn}{$startRow}:{$endColumn}{$startRow}");
    $sheet->mergeCells("{$startColumn}" . ($startRow + 1) . ":{$endColumn}{$endRow}");
    $sheet->setCellValue($labelCell, $label);
    $sheet->setCellValue($valueCell, $value);
    $sheet->getStyle($range)->applyFromArray([
        'fill' => ['fillType' => Fill::FILL_SOLID, 'startColor' => ['rgb' => $fill]],
        'borders' => ['outline' => ['borderStyle' => Border::BORDER_THIN, 'color' => ['rgb' => CASH_REPORT_BORDER]]],
    ]);
    $sheet->getStyle($labelCell)->applyFromArray([
        'font' => ['bold' => true, 'size' => 8, 'color' => ['rgb' => CASH_REPORT_MUTED]],
        'alignment' => ['vertical' => Alignment::VERTICAL_TOP],
    ]);
    $sheet->getStyle($valueCell)->applyFromArray([
        'font' => ['bold' => true, 'size' => 15, 'color' => ['rgb' => CASH_REPORT_NAVY]],
        'alignment' => ['vertical' => Alignment::VERTICAL_BOTTOM, 'horizontal' => Alignment::HORIZONTAL_LEFT],
    ]);
    cashExcelMoneyFormat($sheet, $valueCell);
}

function cashExcelSubmissionDescription(array $transaction): string
{
    $activity = cashExcelTransactionLabel((string) ($transaction['transaction_type'] ?? ''));
    $person = trim((string) ($transaction['person_name'] ?? ''));
    $detail = '';
    foreach (['category_name', 'reason', 'description'] as $field) {
        $candidate = trim((string) ($transaction[$field] ?? ''));
        if ($candidate !== '') {
            $detail = $candidate;
            break;
        }
    }

    $parts = [];
    if ($person !== '') {
        $parts[] = $person;
    }
    if ($detail !== '' && strcasecmp($detail, $person) !== 0) {
        $parts[] = $detail;
    }

    $description = $parts === [] ? $activity : implode(' — ', $parts) . ' · ' . $activity;
    if (!($transaction['affects_balance'] ?? true)) {
        $type = strtoupper((string) ($transaction['transaction_type'] ?? ''));
        $amount = 'NGN ' . number_format((float) ($transaction['amount'] ?? 0), 2, '.', ',');
        $description .= $type === 'MUTILATED_CASH_REPLACEMENT'
            ? ' · ' . $amount . ' restored to usable cash; cashbook balance unchanged'
            : ' · ' . $amount . ' classified as mutilated; cashbook balance unchanged';
    }
    return $description;
}

